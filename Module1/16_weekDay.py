
number = int(input('Podaj numer dnia tygodnia: '))

if number == 1:
    print('Poniedziałek')
elif number == 2:
    print('Wtorek')
elif number == 3:
    print('Środa')
elif number == 4:
    print('Czwartek')
elif number == 5:
    print('Piątek')
elif number == 6:
    print('Sobota')
elif number == 7:
    print('Niedziela')
else:
    print('Entered value is invalid')
